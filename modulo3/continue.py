# Prompt the user to enter a word
user_word = input("Digite uma palavra: ").upper()
# and assign it to the user_word variable.

for letter in user_word:
    # Complete the body of the for loop.
    if letter == "A":
        continue
    elif letter == "E":
        continue
    elif letter == "I":
        continue
    elif letter == "O":
        continue
    elif letter == "U":
        continue
    print(letter)